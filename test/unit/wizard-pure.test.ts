import { describe, it, expect } from "vitest";
import { WizardRunner } from "../../cli/wizard.js";

// assemblePlan is a private method — access via a test subclass or by calling
// it via a prototype inspection. We use a public test by instantiating the runner
// and calling the private method via any-cast.
function assemblePlan(idea: string, plan: string, risks: string, ideas: string[]): string {
  const runner = new WizardRunner("test-project") as unknown as {
    assemblePlan(idea: string, plan: string, risks: string, ideas: string[]): string;
  };
  return runner.assemblePlan(idea, plan, risks, ideas);
}

describe("WizardRunner.assemblePlan (plan.md structure)", () => {
  const result = assemblePlan(
    "Build a todo app",
    "## Architecture\nUse SQLite.",
    "## Risks\n1. Scope creep.",
    ["Round 1 ideas", "Round 2 ideas", "Round 3 ideas"]
  );

  it("starts with the plan heading containing the idea", () => {
    expect(result.startsWith("# Plan: Build a todo app")).toBe(true);
  });

  it("contains a generated-by line with an ISO timestamp", () => {
    expect(result).toMatch(/_Generated by flywheel Planning Wizard/);
  });

  it("contains the plan content verbatim", () => {
    expect(result).toContain("## Architecture\nUse SQLite.");
  });

  it("contains the Adversarial Risk Assessment section", () => {
    expect(result).toContain("## Adversarial Risk Assessment");
    expect(result).toContain("## Risks\n1. Scope creep.");
  });

  it("contains Brilliant Enhancement Ideas section", () => {
    expect(result).toContain("## Brilliant Enhancement Ideas");
  });

  it("contains all three idea rounds with round headings", () => {
    expect(result).toContain("### Round 1");
    expect(result).toContain("Round 1 ideas");
    expect(result).toContain("### Round 2");
    expect(result).toContain("Round 2 ideas");
    expect(result).toContain("### Round 3");
    expect(result).toContain("Round 3 ideas");
  });

  it("with empty ideas array, still has Brilliant Ideas section but no rounds", () => {
    const noIdeas = assemblePlan("idea", "plan", "risks", []);
    expect(noIdeas).toContain("## Brilliant Enhancement Ideas");
    expect(noIdeas).not.toContain("### Round 1");
  });

  it("is deterministic — same args produce same output", () => {
    const a = assemblePlan("idea", "plan", "risks", ["ideas"]);
    const b = assemblePlan("idea", "plan", "risks", ["ideas"]);
    // Only difference may be the generated timestamp — compare structure
    expect(a.split("\n").length).toBe(b.split("\n").length);
  });
});

// isClaudeModel and isGeminiModel are private helpers — test via observable behavior
// by looking at what models the wizard would accept for different providers.
// We replicate the contract here since they're simple prefix checks.

function isClaudeModel(model: string): boolean {
  return model.startsWith("claude-");
}
function isGeminiModel(model: string): boolean {
  return model.startsWith("gemini-");
}

describe("isClaudeModel", () => {
  it("returns true for claude-opus-4-6", () => expect(isClaudeModel("claude-opus-4-6")).toBe(true));
  it("returns true for claude-sonnet-4-6", () => expect(isClaudeModel("claude-sonnet-4-6")).toBe(true));
  it("returns false for gpt-4o", () => expect(isClaudeModel("gpt-4o")).toBe(false));
  it("returns false for gemini-1.5-pro", () => expect(isClaudeModel("gemini-1.5-pro")).toBe(false));
  it("is case-sensitive — CLAUDE- returns false", () => expect(isClaudeModel("CLAUDE-opus")).toBe(false));
  it("returns false for empty string", () => expect(isClaudeModel("")).toBe(false));
});

describe("isGeminiModel", () => {
  it("returns true for gemini-1.5-pro", () => expect(isGeminiModel("gemini-1.5-pro")).toBe(true));
  it("returns true for gemini-pro", () => expect(isGeminiModel("gemini-pro")).toBe(true));
  it("returns false for claude-opus", () => expect(isGeminiModel("claude-opus")).toBe(false));
  it("returns false for empty string", () => expect(isGeminiModel("")).toBe(false));
  it("is case-sensitive — GEMINI- returns false", () => expect(isGeminiModel("GEMINI-pro")).toBe(false));
});
